package ws.model;

public class Sprite {
	private int fX;
	private int fY;
	protected int fId;

	public int getX() {
		return fX;
	}

	public void setX(int x) {
		fX = x;
	}

	public int getY() {
		return fY;
	}

	public void setY(int y) {
		fY = y;
	}

	public int getId() {
		return fId;
	}
}
